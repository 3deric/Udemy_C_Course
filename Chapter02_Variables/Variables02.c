{
    // Declaration
    // int 32 bit (4 Byte)
    int my_age;

    // Init
    my_age = 37;

    // int 32 bit (4 Byte)
    unsigned int my_age2 = 37U;

    // floating point number 32 bit (4 Byte)
    float my_balance1 = 1820.50F;
    // floating point number 64 bit (8 Byte)
    double my_balance2 = 1820.50;

    return 0;
}
